   		<H3 class=headline><SMALL><I>Q1. How do I create sub-points by different speakers under a single talk?</I></SMALL></H3>

   		<BLOCKQUOTE>
   		<SMALL>First, go to the <A HREF="modify.php">modification area</A>, click on the talk title to open the <A HREF="talk_toolbar.php">talk toolbar</A>, and choose "Add Sub Point".<BR></SMALL>
   		</BLOCKQUOTE>

   		<A name="Q2"></A>
   		<H3 class=headline><SMALL><I>Q2. How can I associate minutes to my agenda?</I></SMALL></H3>
	
   		<BLOCKQUOTE>
 			<SMALL>See the "<A HREF="attach.php">attach files</A>" section. Once you have transfered your file (it appears in the <A HREF="list_of_files.php">list of files</A>), you can tell the system this file is of type "minutes".</SMALL>
   		</BLOCKQUOTE>

   		<A name="Q3"></A>
		<H3 class=headline><SMALL><I>Q3. How can I associate a powerpoint presentation to a talk?</I></SMALL></H3>

   		<BLOCKQUOTE>
 			<SMALL>You can adopt two strategies:<BR><BR></SMALL>
			<UL>
			<LI><SMALL>You can convert your presentation into PDF format. We provide such a conversion in our "<A HREF="http://documents.cern.ch/Convert">Convert</A>" module. Then transfer you single PDF file using the <A HREF="file_upload.php">upload facility</A>.</SMALL>
			<LI><SMALL>Or simply transfer your .ppt files with the <A HREF="file_upload.php">upload facility</A>. But remember all platforms will not be able to read powerpoint presentations!!</SMALL></UL>
   		</BLOCKQUOTE>

   		<A name="Q4"></A>
		<H3 class=headline><SMALL><I>Q4. How can I protect the access to my agenda?</I></SMALL></H3>

   		<BLOCKQUOTE>
 			<SMALL>You can choose between two protection levels on your agenda:<BR><BR>
			Restrict the access to CERN computers or<BR>
			Restrict the access with a password.<BR><BR>
			In both case, you should go to the <A HREF="modify.php">modification area</A> for your agenda, and then open the <A HREF="agenda_toolbar.php">agenda toolbar</A>, there choose "Edit agenda data" and modify the "confidentiality level" (if you choose the password, enter it in the "access password" input box).</SMALL>
   		</BLOCKQUOTE>

   		<A name="Q5"></A>
		<H3 class=headline><SMALL><I>Q5. I have recurrent (weekly or monthly) meetings, is there an easy way to create such agendas?</I></SMALL></H3>

   		<BLOCKQUOTE>
 			<SMALL>Once you have created the agenda of your first meeting, and provided all meetings have more or less the same structure, you can clone the first one into its successors. Go in the modification area of the father meeting, click on the meetings title to open the <A HREF="agenda_toolbar.php">agenda toolbar</A>, and click on "clone agenda".</SMALL>
   		</BLOCKQUOTE>
