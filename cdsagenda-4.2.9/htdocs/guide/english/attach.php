You can attach files to any item in your agenda (global object, session, talk). The files you attach to an item can be transfered to our server or linked from another server.<BR>
To attach a file to an agenda, you should first go to the <A HREF="modify.php">modification area</A> of the agenda.
<BR><BR>
Then, choose the item you want to attach a file to (agenda, session or talk) and click on its title. This will open the <A HREF="agenda_toolbar.php">agenda</A>, <A HREF="session_toolbar.php">session</A> or <A HREF="talk_toolbar.php">talk</A> toolbar.<BR><BR>
Using this menu, you have access to the <A HREF="list_of_files.php">list of all the files</A> attached to the item, and you can attach a new file to the item. There are several ways of attaching a file. You can transfer it by <A HREF="file_upload.php">upload</A>. You can <A HREF="scanning_request.php">scan</A> the documents which will be automatically attched to the desired item, or you can <A HREF="file_linkage.php">make a link</A> from the item to a file stored in another server.<BR><BR>
If you choose the last solution, you will be responsible for the stability and availability of the file.<BR><BR>
The last way for attaching a minutes file to a talk, is to write your minutes directly in an input box (see "<A HREF="talk_toolbar.php">talk toolbar</A>"). Once you have finished and validated your writting, a new text file will appear in the <A HREF="list_of_files.php">list of files</A> containing your minutes.<BR><BR>
The supported file formats are: .doc .ps .pdf .ppt .jpeg .gif.

