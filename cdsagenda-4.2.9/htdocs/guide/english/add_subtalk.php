A subtalk is only a point in a presentation/talk, so unlike the talk, you don't specify here any starting time nor room (this information is the same as the father talk). <BR>You must fill in the fields on this form page, then click on the "Add Subtalk" button.</SMALL><BR><BR>
		<SMALL><FONT color=red>Description of the fields in the creation form:</FONT></SMALL>
			<BLOCKQUOTE>

<TABLE cellspacing=10 border=0>
<TR>
   <TD valign=top align=right><B><SMALL>Title</SMALL></B></TD>
   <TD><SMALL>Enter here the name of the new subpoint</SMALL></TD>
</TR>
<TR>
   <TD valign=top align=right><B><SMALL>Speaker</SMALL></B></TD>
   <TD><SMALL>Name of the speaker for this sub-point (preferably formatted like that: Name, Initial(s))</SMALL></TD>
</TR>
<TR>
   <TD valign=top align=right><B><SMALL>Speaker's affiliation</SMALL></B></TD>
<TD><SMALL>This optional field may be used to specified the home institute/company of the speaker.</SMALL></TD>
</TR>
<TR>
<TD valign=top align=right><B><SMALL>Contact e-mail</SMALL></B></TD>
<TD><SMALL>E-mail address of the speaker. This field is optional. If you enter it, the name of the chairman will become clickable, allowing the user to send him directly an e-mail.</SMALL></TD>
</TR>
<TR>
<TD valign=top align=right><B><SMALL>Duration</SMALL></B></TD>
<TD><SMALL>Enter here the duration of the point.</SMALL></TD>
</TR>
<TR>
<TD valign=top align=right><B><SMALL>Report Number</SMALL></B></TD>
<TD><SMALL>If this point (or the documents related to it) are stored in a document server, you can enter a document number here.</SMALL></TD>
</TR>
<TR>
<TD valign=top align=right><B><SMALL>Description</SMALL></B></TD>
<TD><SMALL>This is a free text displayed to the user to specify special notes or comments, or th abstract of the talk.</SMALL></TD>
</TR>

