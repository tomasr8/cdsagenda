		The other existing formats are (among others):</SMALL>
		<UL>
			<LI><SMALL><B>Standard Meeting (multi-sessions)</B> This is the basic format. It includes all possible information about a meeting constituted of several sessions.</SMALL>
			<LI><SMALL><B>Standard Meeting (single-session)</B> This format includes all possible information about a meeting constituted of one and only one session. (simple meeting)</SMALL>
			<LI><SMALL><B>Standard Meeting (inline minutes)</B> Basic format which includes the minutes text instead of the links to the corresponding files. (the inline minutes are text conversions from PostScript, PDF, HTML and ascii text files). The files must be stored as "minutes" type of material for the program to recognise them and display them.</SMALL>
			<LI><SMALL><B>all minutes in one file</B> This format includes all attached minutes (see comments above) on one html page.</SMALL>
			<LI><SMALL><B>administrative</B> This format has been set up for displaying the agendas of official CERN meetings (Finance Committee Meetings...). Easier to print than the default format.<BR><BR></SMALL>
			<LI><SMALL><B>compact</B> This format presents a very compact, printable version of an agenda. It does not display the abstract of the talks, nor the links to the attached files.</SMALL>
			<LI><SMALL><B>lecture</B> This format is used to nicely present an agenda containing only one talk (lecture or seminar)</SMALL>
			<LI><SMALL><B>event</B> This format is used to nicely present an agenda containing only basic informations about one event. Usually this special agenda will not contain any sessions nor talks.</SMALL>
		</UL>
		<SMALL>These formats are accessible from the <b>[change view]</b> menu of the default agenda page. (see "default format").

